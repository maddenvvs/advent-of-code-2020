from aoc2020.solutions.day_20 import Arrangement, \
    Image, \
    count_product_of_corners_in_arrangement, \
    border_value, \
    count_water_roughness


def test_configuration_border():
    test_borders = [("#", 1), ("#.", 1), (".#", 2),
                    ("#.#", 5), ("..##.#...", 44)]

    for border, number in test_borders:
        assert border_value(border) == number


def test_borders_initial_image():
    test_image = [
        list("..##."),
        list("##..#"),
        list("#...#"),
        list("####."),
        list("##.##"),
    ]

    assert Image(test_image).borders() == (12, 22, 27, 30)


def test_borders_flipped_horizontally():
    test_image = [
        list("..##."),
        list("##..#"),
        list("#...#"),
        list("####."),
        list("##.##"),
    ]

    assert Image(test_image).flip_horizontally().borders() == (27, 13, 12, 15)


def test_borders_flipped_vertically():
    test_image = [
        list("..##."),
        list("##..#"),
        list("#...#"),
        list("####."),
        list("##.##"),
    ]

    assert Image(test_image).flip_vertically().borders() == (6, 30, 27, 22)


